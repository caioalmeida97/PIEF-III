    private void botao_busca_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_busca_clienteActionPerformed
        try {
            if (!checkbox_cnpj_buscar_cliente.isSelected()
                    && !checkbox_nome_buscar_cliente.isSelected()) {
                throw new Exception ("Selecione uma forma de busca!");
            }
            if(campo_busca_cliente.getText().equals("")){
                throw new Exception ("Preencha o campo de busca!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botao_busca_clienteActionPerformed

